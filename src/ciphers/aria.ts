// Export the ARIA S-boxes for the involutory demo (sb1 is the AES S-box, is1 is its inverse)
export { sb1 as ARIA_SB1, sb2 as ARIA_SB2, is1 as ARIA_IS1, is2 as ARIA_IS2 };

// Helper: read/write uint32 little-endian
function getLE32(b: Uint8Array, o: number): number {
  return (b[o] | (b[o+1] << 8) | (b[o+2] << 16) | (b[o+3] << 24)) >>> 0;
}
function putLE32(b: Uint8Array, o: number, v: number): void {
  b[o] = v & 0xff; b[o+1] = (v >>> 8) & 0xff;
  b[o+2] = (v >>> 16) & 0xff; b[o+3] = (v >>> 24) & 0xff;
}

function BYTE0(x: number): number { return x & 0xff; }
function BYTE1(x: number): number { return (x >>> 8) & 0xff; }
function BYTE2(x: number): number { return (x >>> 16) & 0xff; }
function BYTE3(x: number): number { return (x >>> 24) & 0xff; }

function BSWAP32(x: number): number {
  return (((x & 0xff) << 24) | (((x >>> 8) & 0xff) << 16) |
          (((x >>> 16) & 0xff) << 8) | ((x >>> 24) & 0xff)) >>> 0;
}

function P1(x: number): number { return (((x >>> 8) & 0x00ff00ff) | (((x & 0x00ff00ff) << 8) >>> 0)) >>> 0; }
function P2(x: number): number { return (((x >>> 16) | (x << 16)) >>> 0); }
function P3(x: number): number { return BSWAP32(x); }

// ARIA Affine transform A
function ariaA(s: Uint32Array): void {
  let a = s[0], b = s[1], c = s[2], d = s[3];
  let ta: number, tb: number, tc: number;
  ta = b;
  b = a;
  a = P2(ta);
  tb = P2(d);
  d = P1(c);
  c = P1(tb);
  ta = (ta ^ d) >>> 0;
  tc = P2(b);
  ta = (P1(ta) ^ tc ^ c) >>> 0;
  tb = (tb ^ P2(d)) >>> 0;
  tc = (tc ^ P1(a)) >>> 0;
  b = (b ^ ta ^ tb) >>> 0;
  tb = (P2(tb) ^ ta) >>> 0;
  a = (a ^ P1(tb)) >>> 0;
  ta = P2(ta);
  d = (d ^ P1(ta) ^ tc) >>> 0;
  tc = P2(tc);
  c = (c ^ P1(tc) ^ ta) >>> 0;
  s[0] = a; s[1] = b; s[2] = c; s[3] = d;
}

// Substitution layer
function ariaSL(s: Uint32Array, sa: Uint8Array, sb_: Uint8Array, sc: Uint8Array, sd: Uint8Array): void {
  for (let i = 0; i < 4; i++) {
    s[i] = (sa[BYTE0(s[i])] | (sb_[BYTE1(s[i])] << 8) | (sc[BYTE2(s[i])] << 16) | (sd[BYTE3(s[i])] << 24)) >>> 0;
  }
}

// FO helper for key schedule: r = FO(p, k) ^ x
function ariaFoXor(r: Uint32Array, p: Uint32Array, k: Uint32Array, x: Uint32Array): void {
  const s = new Uint32Array(4);
  for (let i = 0; i < 4; i++) s[i] = (p[i] ^ k[i]) >>> 0;
  ariaSL(s, sb1, sb2, is1, is2);
  ariaA(s);
  for (let i = 0; i < 4; i++) r[i] = (s[i] ^ x[i]) >>> 0;
}

// FE helper for key schedule: r = FE(p, k) ^ x
function ariaFeXor(r: Uint32Array, p: Uint32Array, k: Uint32Array, x: Uint32Array): void {
  const s = new Uint32Array(4);
  for (let i = 0; i < 4; i++) s[i] = (p[i] ^ k[i]) >>> 0;
  ariaSL(s, is1, is2, sb1, sb2);
  ariaA(s);
  for (let i = 0; i < 4; i++) r[i] = (s[i] ^ x[i]) >>> 0;
}

// 128-bit rotation for key schedule
function ariaRot128(r: Uint32Array, ro: number, a: Uint32Array, b: Uint32Array, n: number): void {
  const n1 = n % 32;
  const n2 = n1 ? 32 - n1 : 0;
  let j = (Math.floor(n / 32)) % 4;
  let t = P3(b[j]);
  for (let i = 0; i < 4; i++) {
    j = (j + 1) % 4;
    const u = P3(b[j]);
    t = ((t << n1) | (n2 ? (u >>> n2) : 0)) >>> 0;
    t = P3(t);
    r[ro + i] = (a[i] ^ t) >>> 0;
    t = u;
  }
}

// Round constants
const RC: number[][] = [
  [0xB7C17C51, 0x940A2227, 0xE8AB13FE, 0xE06E9AFA],
  [0xCC4AB16D, 0x20C8219E, 0xD5B128FF, 0xB0E25DEF],
  [0x1D3792DB, 0x70E92621, 0x75972403, 0x0EC9E804]
];

interface AriaCtx {
  nr: number;
  rk: Uint32Array[]; // round keys, each is 4 uint32
}

function ariaSetKeyEnc(key: Uint8Array, keybits: number): AriaCtx {
  const w: Uint32Array[] = [new Uint32Array(4), new Uint32Array(4), new Uint32Array(4), new Uint32Array(4)];
  w[0][0] = getLE32(key, 0); w[0][1] = getLE32(key, 4);
  w[0][2] = getLE32(key, 8); w[0][3] = getLE32(key, 12);
  w[1].fill(0);
  if (keybits >= 192) { w[1][0] = getLE32(key, 16); w[1][1] = getLE32(key, 20); }
  if (keybits === 256) { w[1][2] = getLE32(key, 24); w[1][3] = getLE32(key, 28); }

  let ci = (keybits - 128) >> 6; // 0, 1, or 2
  const nr = 12 + 2 * ci;

  const rc0 = new Uint32Array(RC[ci]);
  ariaFoXor(w[1], w[0], rc0, w[1]);
  ci = ci < 2 ? ci + 1 : 0;
  const rc1 = new Uint32Array(RC[ci]);
  ariaFeXor(w[2], w[1], rc1, w[0]);
  ci = ci < 2 ? ci + 1 : 0;
  const rc2 = new Uint32Array(RC[ci]);
  ariaFoXor(w[3], w[2], rc2, w[1]);

  const rk: Uint32Array[] = [];
  for (let i = 0; i <= 16; i++) rk.push(new Uint32Array(4));

  for (let i = 0; i < 4; i++) {
    const w2 = w[(i + 1) & 3];
    ariaRot128(rk[i], 0, w[i], w2, 128 - 19);
    ariaRot128(rk[i + 4], 0, w[i], w2, 128 - 31);
    ariaRot128(rk[i + 8], 0, w[i], w2, 61);
    ariaRot128(rk[i + 12], 0, w[i], w2, 31);
  }
  ariaRot128(rk[16], 0, w[0], w[1], 19);

  return { nr, rk };
}

function ariaSetKeyDec(key: Uint8Array, keybits: number): AriaCtx {
  const enc = ariaSetKeyEnc(key, keybits);
  const { nr, rk } = enc;
  // Flip round keys
  for (let i = 0, j = nr; i < j; i++, j--) {
    for (let k = 0; k < 4; k++) {
      const t = rk[i][k]; rk[i][k] = rk[j][k]; rk[j][k] = t;
    }
  }
  // Apply affine transform to middle keys
  for (let i = 1; i < nr; i++) ariaA(rk[i]);
  return { nr, rk };
}

function ariaCryptEcb(ctx: AriaCtx, input: Uint8Array): Uint8Array {
  const output = new Uint8Array(16);
  const s = new Uint32Array(4);
  s[0] = getLE32(input, 0); s[1] = getLE32(input, 4);
  s[2] = getLE32(input, 8); s[3] = getLE32(input, 12);

  let i = 0;
  while (true) {
    for (let k = 0; k < 4; k++) s[k] = (s[k] ^ ctx.rk[i][k]) >>> 0;
    i++;
    ariaSL(s, sb1, sb2, is1, is2);
    ariaA(s);

    for (let k = 0; k < 4; k++) s[k] = (s[k] ^ ctx.rk[i][k]) >>> 0;
    i++;
    ariaSL(s, is1, is2, sb1, sb2);
    if (i >= ctx.nr) break;
    ariaA(s);
  }

  // Final key mixing
  for (let k = 0; k < 4; k++) s[k] = (s[k] ^ ctx.rk[i][k]) >>> 0;

  putLE32(output, 0, s[0]); putLE32(output, 4, s[1]);
  putLE32(output, 8, s[2]); putLE32(output, 12, s[3]);
  return output;
}

export class Aria {
  private encCtx: AriaCtx;
  private decCtx: AriaCtx;
  constructor(key: Uint8Array) {
    const keybits = key.length * 8;
    if (keybits !== 128 && keybits !== 192 && keybits !== 256)
      throw new Error('ARIA: key must be 16, 24, or 32 bytes');
    this.encCtx = ariaSetKeyEnc(key, keybits);
    this.decCtx = ariaSetKeyDec(key, keybits);
  }
  encryptBlock(block: Uint8Array): Uint8Array {
    if (block.length !== 16) throw new Error('Block must be 16 bytes');
    return ariaCryptEcb(this.encCtx, block);
  }
  decryptBlock(block: Uint8Array): Uint8Array {
    if (block.length !== 16) throw new Error('Block must be 16 bytes');
    return ariaCryptEcb(this.decCtx, block);
  }
  getRounds(): number { return this.encCtx.nr; }
}
